/**
 * Plus (+) operator for datatype Decimal.
 */
public class PlusDecimal extends AbstractUnaryOperation {

    public PlusDecimal() {
        super(Datatype.DECIMAL, "+"); //$NON-NLS-1$
    }

    /** 
     * {@inheritDoc}
     */
    public CompilationResultImpl generate(CompilationResultImpl arg) {
        return arg;
    }

}
