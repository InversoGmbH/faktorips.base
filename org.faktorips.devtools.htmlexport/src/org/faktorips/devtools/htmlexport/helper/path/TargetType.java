package org.faktorips.devtools.htmlexport.helper.path;

public enum TargetType {
    CLASSES,
    CONTENT,
    OVERALL;

    public String getId() {
        if (this.equals(OVERALL)) {
            return "_top"; //$NON-NLS-1$
        }
        return name().toLowerCase();
    }
}
